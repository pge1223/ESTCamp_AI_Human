import numpy as np

array = np.array([1,2,3,4,5])
print(array)


# Q1. arrayм—җ 5лҘј лҚ”н•ң к°’мқ„ м¶ңл Ҙн•ҙліҙм„ёмҡ”.
print(array+5)

# Q2. arrayм—җ 5лҘј ләҖ к°’мқ„ м¶ңл Ҙн•ҙліҙм„ёмҡ”.
print(array-5)

# Q3. arrayм—җ 5лҘј кіұн•ң к°’мқ„ м¶ңл Ҙн•ҙліҙм„ёмҡ”.
print(array*5)

# Q4. arrayлҘј 5лЎң лӮҳлҲҲ к°’мқ„ м¶ңл Ҙн•ҙліҙм„ёмҡ”.
print(array/5)


# Q5. arrayм—җ array2лҘј лҚ”н•ң к°’мқ„ м¶ңл Ҙн•ҙліҙм„ёмҡ”.    
array2 = np.array([5,4,3,2,1])
print(array+array2)


# Q6. arrayм—җ array2лҘј ләҖ к°’мқ„ м¶ңл Ҙн•ҙліҙм„ёмҡ”.
print(array-array2)

